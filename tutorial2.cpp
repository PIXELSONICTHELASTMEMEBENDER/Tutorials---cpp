#include <iostream>
#include <string>
using namespace std;

int main() {
	
	// Defining Names
	const string nameofCoder;

	const int ageofCoder;
	 
	const bool experiencedCoder;

	// Inserting Values into Variable(s)
	nameofCoder = "Quantum";
	
	ageofCoder = 7;
	
	experiencedCoder = true;

	// Printing the Values from the Variable(s)
	cout << nameofCoder << endl; 
	
	cout << ageofCoder << endl;
	
	cout << experiencedCoder << endl;

	// Another way of printing out the value of all of the variables at the same time
	cout << nameofCoder << endl << ageofCoder << endl << experiencedCoder << endl;

}

