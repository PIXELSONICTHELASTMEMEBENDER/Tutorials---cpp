#include <iostream>
#include <string>
using namespace std;

int main() {
	// Order of Operations
	int oo1 = 10;
	int oo2 = 8;
	int ooo = (oo1 + oo2) + 2 * 2 / 3 / 200 + 23 - 34 - 0 - 3;
	cout << ooo << endl;

	// Addition Operator
	float x = 0.8;
	int y = 9;
	int sum = x + y; 
	cout << sum << endl;

	// Subtraction Operator
	float a = 1.34;
	int b = 10;
	int difference = a - b;
	cout << difference << endl;

	// Multiplication Operator
	int c = 1;
	float f = 10.1;
	int answer1 = c * f;
	cout << answer1 << endl;

	// Division Operator
	float m = 50;
	float n = 10.9;
	float answer2 = m/n;
	cout << answer2 << endl;

	// Compound Assignment Operator
	float md = 8;
	md += 2;
	cout << md << endl;

	float bo = 9;
	bo -= 2;
	cout << bo << endl;

	float number = 10;
	number++; // Adds 1
	cout << number << endl;

}
